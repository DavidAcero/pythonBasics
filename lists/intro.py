'''
There are four collection data types in the Python programming language:

List       : collection which is ordered and changeable. Allows duplicate members.
Tuple      : collection which is ordered and unchangeable. Allows duplicate members.
Set        : collection which is unordered and unindexed. No duplicate members.
Dictionary : collection which is unordered and changeable. No duplicate members.
'''

countries = ["Australia", "Mexico", "Canada", "Russia", "US" ,"Spain", "Egypt"]
print(countries)
print(countries[2])
print(countries[2:5])
print(countries[-4:-2])
print(len(countries))